    private void consulterEntresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consulterEntresActionPerformed
        try{
            DefaultTableModel tableModelEntres= (DefaultTableModel)tableEntres.getModel();
            List<Produit> products = new ArrayList<>();
            Class.forName("com.mysql.cj.jdbc.Driver");
            // database is my_tutorial, userid =root, password //
            // Update your usrid, password and database name //
            Connection con=DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/gestionstock","root","");
            Statement st=con.createStatement();

            // Adding record
            String sql="select * from  produit";
            ResultSet  resultat = st.executeQuery(sql);
            while(resultat.next()){
                int id = resultat.getInt("id");
                String nomProduit = resultat.getString("nom_produit");
                String qantiteProduit = resultat.getString("qantit_produit");
                Produit produit= new Produit();
                produit.setId(id);
                produit.setNomProduit(nomProduit);
                produit.setQuantiteProduit(qantiteProduit);
                products.add(produit);
                // Produit tableEntre[]={produit.class};

            }
            for (int i = 0; i < products.size(); i++) {
                tableModelEntres.insertRow(i, new Object[] {
                    products.get(i).getId(),
                    products.get(i).getNomProduit(),
                    products.get(i).getQuantiteProduit(),

                });
            }

            con.close();

        }catch(Exception e){ System.out.println(e);}
    }//GEN-LAST:event_consulterEntresActionPerformed

